      if (price == undefined || price <= 0) price = 0;
      return "￥" + Math.round(price).toLocaleString("ja-JP");
    },
    groupBy: (items, key) => items.reduce(
      (result, item) => ({
        ...result,
        [item[key]]: [
          ...(result[item[key]] || []),
          item,
        ],
      }), {},
    ),
  }
};
